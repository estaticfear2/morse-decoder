const MORSE_TABLE = {
  '.-':     'a',
  '-...':   'b',
  '-.-.':   'c',
  '-..':    'd',
  '.':      'e',
  '..-.':   'f',
  '--.':    'g',
  '....':   'h',
  '..':     'i',
  '.---':   'j',
  '-.-':    'k',
  '.-..':   'l',
  '--':     'm',
  '-.':     'n',
  '---':    'o',
  '.--.':   'p',
  '--.-':   'q',
  '.-.':    'r',
  '...':    's',
  '-':      't',
  '..-':    'u',
  '...-':   'v',
  '.--':    'w',
  '-..-':   'x',
  '-.--':   'y',
  '--..':   'z',
  '.----':  '1',
  '..---':  '2',
  '...--':  '3',
  '....-':  '4',
  '.....':  '5',
  '-....':  '6',
  '--...':  '7',
  '---..':  '8',
  '----.':  '9',
  '-----':  '0',
  ' ':  ' ',
};


const DIGIT_TABLE = {
  '10': '.',
  '11': '-',
  ' ': ' ',
};

function decode(expr) {
  const digitExpr = expr.match(/.{1,10}/g).map(it => +it ? +it +'' : ' ');
  
  return digitExpr.map(it => it.match(/.{1,2}/g)
    .map(it => DIGIT_TABLE[it]))
    .map(it => MORSE_TABLE[it.join('')])
    .join('');
}


module.exports = {
  decode
}
